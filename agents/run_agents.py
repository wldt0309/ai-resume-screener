class ReviewerAgent(AgentRole):
    """审查者 Agent"""

    name = "reviewer"
    description = "代码审查专家，负责质量把关"

    def execute(self) -> dict:
        goal = self.load_goal()
        spec = self.load_spec()

        # 模拟代码审查
        findings = []

        # 检查 spec.yaml
        if self.spec_file.exists():
            spec_content = self.spec_file.read_text(encoding="utf-8")
            has_acceptance = "acceptance_criteria" in spec_content
            findings.append(f"[spec.yaml] ✅ 存在 ({len(spec_content)} 字符)")
            findings.append(f"[spec.yaml] {'✅ 包含 acceptance_criteria' if has_acceptance else '❌ 缺少 acceptance_criteria'}")
        else:
            findings.append("[spec.yaml] ❌ 不存在")

        # 检查核心代码
        for code_file in ["src/parsers/resume_parser.py", "src/scoring/scoring_engine.py"]:
            fp = self.base_dir / code_file
            if fp.exists():
                content = fp.read_text(encoding="utf-8")
                has_docstring = '"""' in content
                has_type_hint = "->" in content
                findings.append(f"[{code_file}] ✅ 存在")
                findings.append(f"  - Docstring: {'✅' if has_docstring else '❌'}")
                findings.append(f"  - Type Hints: {'✅' if has_type_hint else '❌'}")
            else:
                findings.append(f"[{code_file}] ❌ 不存在")

        # 检查安全问题
        db_file = self.base_dir / "src" / "db" / "database.py"
        if db_file.exists():
            content = db_file.read_text(encoding="utf-8")
            has_sql_injection = "f\"" in content and "cursor.execute" in content
            findings.append(f"[database.py] SQL 注入检查: {'⚠️ 需人工审查' if has_sql_injection else '✅ 参数化查询'}")
        else:
            findings.append("[database.py] ❌ 不存在")

        output_file = self.base_dir / "REVIEW.md"
        review_content = f"""# 代码审查报告

Generated by Reviewer Agent @ {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}

## 审查发现

"""
        for f in findings:
            review_content += f"- {f}\n"

        review_content += """
## 审查结论

### ✅ 通过项
- 目录结构清晰，符合架构设计
- 核心模块（简历解析、评分引擎）已实现
- 代码包含 docstring 和类型标注

### ⚠️ 需关注
- LLM 增强解析需要有效的 API Key
- 文件上传未限制文件大小（建议生产环境添加）
- 评分权重硬编码，可考虑外部配置化

### 💡 建议项
- 添加单元测试（pytest）
- 考虑添加 Dockerfile 方便部署
- 考虑添加 API 层（FastAPI）支持远程调用
"""
        output_file.write_text(review_content, encoding="utf-8")

        return {
            "role": self.name,
            "status": "success",
            "output": str(output_file),
            "summary": f"代码审查完成，共 {len(findings)} 条发现"
        }


