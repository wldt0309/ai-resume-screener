# ============ Agent 工厂 ============
def get_agent(role_name: str) -> AgentRole:
    """根据角色名获取 Agent 实例"""
    agents = {
        "architect": ArchitectAgent,
        "developer": DeveloperAgent,
        "reviewer": ReviewerAgent,
        "qa": QAAgent,
    }
    agent_class = agents.get(role_name.lower())
    if not agent_class:
        raise ValueError(f"未知角色: {role_name}")
    return agent_class()


# ============ 主流程 ============
def run_pipeline(roles: Optional[list[str]] = None) -> None:
    """
    运行 Agent 流水线

    Args:
        roles: 要执行的角色列表，None 表示全部
    """
    print("=" * 60)
    print("🚀 GSD + OpenSpec + Superpowers + gstack 流水线")
    print("=" * 60)
    print(f"📁 项目目录: {BASE_DIR}")
    print(f"⏰ 开始时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print()

    # 默认执行顺序
    all_roles = ["architect", "developer", "reviewer", "qa"]
    roles_to_run = roles or all_roles

    results = []
    for role_name in roles_to_run:
        print(f"\n{'=' * 60}")
        print(f"🤖 [{role_name.upper()}] Agent 执行中...")
        print(f"{'=' * 60}")

        try:
            agent = get_agent(role_name)
            result = agent.execute()
            results.append(result)
            print(f"✅ [{role_name.upper()}] 完成")
            print(f"   输出: {result['output']}")
            print(f"   摘要: {result['summary']}")
        except Exception as e:
            print(f"❌ [{role_name.upper()}] 失败: {e}")
            results.append({
                "role": role_name,
                "status": "error",
                "error": str(e)
            })

    # 汇总
    print()
    print("=" * 60)
    print("📊 流水线执行汇总")
    print("=" * 60)

    for r in results:
        status_icon = "✅" if r["status"] == "success" else "❌"
        print(f"  {status_icon} {r['role']}: {r['status']}")

    success_count = sum(1 for r in results if r["status"] == "success")
    print(f"\n完成: {success_count}/{len(results)}")

    print()
    print("📋 产出物清单:")
    for f in ["GOAL.md", "spec.yaml", "ARCHITECTURE.md",
               "DEVELOPER_STATUS.md", "REVIEW.md", "TEST_REPORT.md"]:
        fp = BASE_DIR / f
        status = "✅" if fp.exists() else "❌"
        print(f"  {status} {f}")

    print()
    print(f"⏰ 结束时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60)


# ============ CLI 入口 ============
def main():
    parser = argparse.ArgumentParser(
        description="gstack Agent 流水线执行器"
    )
    parser.add_argument(
        "--role",
        choices=["architect", "developer", "reviewer", "qa", "all"],
        default="all",
        help="执行特定角色或全部 (default: all)"
    )
    parser.add_argument(
        "--list",
        action="store_true",
        help="列出所有可用角色"
    )

    args = parser.parse_args()

    if args.list:
        print("可用角色:")
        for r in ["architect", "developer", "reviewer", "qa"]:
            agent = get_agent(r)
            print(f"  - {r}: {agent.description}")
        return

    if args.role == "all":
        run_pipeline()
    else:
        run_pipeline([args.role])


if __name__ == "__main__":
    main()
