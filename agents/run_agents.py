# ============ 角色定义 ============
class AgentRole:
    """角色基类"""

    name: str = "base"
    description: str = ""

    def __init__(self, base_dir: Path = BASE_DIR):
        self.base_dir = base_dir
        self.goal_file = base_dir / "GOAL.md"
        self.spec_file = base_dir / "spec.yaml"
        self.output_file: Optional[Path] = None

    def load_goal(self) -> str:
        if self.goal_file.exists():
            return self.goal_file.read_text(encoding="utf-8")
        return ""

    def load_spec(self) -> str:
        if self.spec_file.exists():
            return self.spec_file.read_text(encoding="utf-8")
        return ""

    def load_prompt(self) -> str:
        prompt_file = self.base_dir / "agents" / "prompts" / f"{self.name}.md"
        if prompt_file.exists():
            return prompt_file.read_text(encoding="utf-8")
        return ""

    def execute(self) -> dict:
        """执行角色任务，返回结果"""
        raise NotImplementedError


